import * as parser from "@babel/parser";
import traverse from "@babel/traverse";
import * as t from "@babel/types";


const code = `function square(n) {
  return n * n;
}`;

const ast = parser.parse(code);

traverse(ast, {
  enter(path) {
    if (path.isIdentifier({ name: "n" })) {
      path.node.name = "x";
    }
  },
});